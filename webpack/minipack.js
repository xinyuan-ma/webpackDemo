// è¯¥`presets`é€‰é¡¹æ˜¯ä¸€ç»„è§„åˆ™,å‘Šè¯‰`babel`å¦‚ä½•ä¼ è¾“æˆ‘ä»¬çš„ä»£ç .
// æˆ‘ä»¬ç”¨`babel-preset-env``å°†æˆ‘ä»¬çš„ä»£ç è½¬æ¢ä¸ºæµè§ˆå™¨å¯ä»¥è¿è¡Œçš„ä¸œè¥¿.
