  const content = fs.readFileSync(filename, 'utf-8');

//   ç°åœ¨æˆ‘ä»¬è¯•å›¾æ‰¾å‡ºè¿™ä¸ªæ–‡ä»¶ä¾èµ–äºå“ªä¸ªæ–‡ä»¶. æˆ‘ä»¬å¯ä»¥é€šè¿‡æŸ¥çœ‹å…¶å†…å®¹

