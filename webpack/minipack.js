// æœ€åˆ è¿™ä¸ªé˜Ÿåˆ— åªæœ‰ä¸€ä¸ª èµ„äº§,ä½†æ˜¯å½“æˆ‘ä»¬è¿­ä»£å®ƒæ—¶,æˆ‘ä»¬ä¼šå°†é¢å¤–çš„ æ–°èµ„äº§ æ¨å…¥ é˜Ÿåˆ— ä¸­.
// è¿™ä¸ªå¾ªç¯å°†åœ¨ é˜Ÿåˆ— ä¸ºç©ºæ—¶ç»ˆæ­¢.
  for (const asset of queue) {
    // æˆ‘ä»¬çš„æ¯ä¸€ä¸ª èµ„äº§ éƒ½æœ‰å®ƒæ‰€ä¾èµ–æ¨¡å—çš„ç›¸å¯¹è·¯å¾„åˆ—è¡¨.
    // æˆ‘ä»¬å°†é‡å¤å®ƒä»¬,ç”¨æˆ‘ä»¬çš„`createAsset() `å‡½æ•°è§£æå®ƒä»¬,å¹¶è·Ÿè¸ªæ­¤æ¨¡å—åœ¨æ­¤å¯¹è±¡ä¸­çš„ä¾èµ–å…³ç³».
    asset.mapping = {};

    // è¿™æ˜¯è¿™ä¸ªæ¨¡å—æ‰€åœ¨çš„ç›®å½•.
    const dirname = path.dirname(asset.filename);

    // æˆ‘ä»¬éå†å…¶ç›¸å…³è·¯å¾„çš„åˆ—è¡¨
    asset.dependencies.forEach(relativePath => {
    // æˆ‘ä»¬çš„`createAsset()`å‡½æ•°éœ€è¦ä¸€ä¸ªç»å¯¹æ–‡ä»¶å.
    // ä½†æ˜¯è¯¥ä¾èµ–å…³ç³»æ•°ç»„æ˜¯ä¿å­˜äº†ç›¸å¯¹è·¯å¾„çš„æ•°ç»„.
    // è¿™äº›è·¯å¾„æ˜¯ç›¸å¯¹äºå¯¼å…¥ä»–ä»¬çš„æ–‡ä»¶.
    // æˆ‘ä»¬å¯ä»¥é€šè¿‡å°†ç›¸å¯¹è·¯å¾„ä¸çˆ¶èµ„æºç›®å½•çš„è·¯å¾„è¿æ¥,å°†ç›¸å¯¹è·¯å¾„è½¬å˜ä¸ºç»å¯¹è·¯å¾„.
      const absolutePath = path.join(dirname, relativePath);
      // console.log(absolutePath, 'absolutePath');

      // è§£æèµ„äº§,è¯»å–å…¶å†…å®¹å¹¶æå–å…¶ä¾èµ–å…³ç³».
      const child = createAsset(absolutePath);

      // console.log(child, 'child');

      //   äº†è§£`asset`ä¾èµ–å–å†³äº`child`è¿™ä¸€ç‚¹å¯¹æˆ‘ä»¬æ¥è¯´å¾ˆé‡è¦.
    //   é€šè¿‡ç»™`asset.mapping`å¯¹è±¡å¢åŠ ä¸€ä¸ªæ–°çš„å±æ€§(å€¼ä¸ºchild.id)æ¥è¡¨è¾¾è¿™ç§ä¸€ä¸€å¯¹åº”çš„å…³ç³».
      asset.mapping[relativePath] = child.id;

      // æœ€å,æˆ‘ä»¬å°†`child`è¿™ä¸ªèµ„äº§æ¨å…¥é˜Ÿåˆ—,è¿™æ ·å®ƒçš„ä¾èµ–å…³ç³»ä¹Ÿå°†è¢«è¿­ä»£å’Œè§£æ.
      queue.push(child);
    });
  }

//   åˆ°è¿™ä¸€æ­¥,é˜Ÿåˆ— å°±æ˜¯ä¸€ä¸ªåŒ…å«ç›®æ ‡åº”ç”¨ä¸­ æ¯ä¸ªæ¨¡å— çš„æ•°ç»„:
