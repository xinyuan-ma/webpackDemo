// è¿™ä¸ªæ•°ç»„å°†ä¿å­˜è¿™ä¸ªæ¨¡å—ä¾èµ–çš„æ¨¡å—çš„ç›¸å¯¹è·¯å¾„.
  const dependencies = [];

