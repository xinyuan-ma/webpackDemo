// ç°åœ¨æˆ‘ä»¬å¯ä»¥æå–å•ä¸ªæ¨¡å—çš„ä¾èµ–å…³ç³»,æˆ‘ä»¬å°†é€šè¿‡æå–`å…¥å£æ–‡ä»¶{entry}`çš„ä¾èµ–å…³ç³»æ¥è§£å†³é—®é¢˜.
// é‚£ä¹ˆ,æˆ‘ä»¬å°†æå–å®ƒçš„æ¯ä¸€ä¸ªä¾èµ–å…³ç³»çš„ä¾èµ–å…³ç³». å¾ªç¯ä¸‹å»
