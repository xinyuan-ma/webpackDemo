  const id = ID++;

//   æˆ‘ä»¬ä½¿ç”¨`Ecmascript`æ¨¡å—å’Œå…¶ä»–JavaScriptåŠŸèƒ½,å¯èƒ½ä¸æ”¯æŒæ‰€æœ‰æµè§ˆå™¨.
//   ä¸ºäº†ç¡®ä¿`æˆ‘ä»¬çš„bundle`åœ¨æ‰€æœ‰æµè§ˆå™¨ä¸­è¿è¡Œ,
